CREATE TABLE THAMGIADT
(
	MAGV VARCHAR(5),
	MADT VARCHAR(5),
	STT INT,
	PHUCAP DECIMAL(10,2),
	KETQUA NVARCHAR(5)

	CONSTRAINT PK_TGDT
	PRIMARY KEY(MAGV,MADT,STT)
)

CREATE TABLE CONGVIEC
(
	MADT VARCHAR(5),
	SOTT INT,
	TENCV NVARCHAR(50),
	NGAYBD DATE,
	NGAYKT DATE

	CONSTRAINT PK_CV
	PRIMARY KEY(MADT,SOTT)
)

CREATE TABLE DETAI
(
	MADT VARCHAR(5),
	TENDT NVARCHAR(50),
	CAPQL NVARCHAR(50),
	KINHPHI DECIMAL(10,2),
	NGAYBD DATE,
	NGAYKT DATE,
	MACD VARCHAR(5),
	GVCNDT VARCHAR(5)

	CONSTRAINT PK_DT
	PRIMARY KEY(MADT)
)

CREATE TABLE CHUDE
(
	MACD VARCHAR(5),
	TENCD NVARCHAR(50)

	CONSTRAINT PK_CD
	PRIMARY KEY(MACD)
)

CREATE TABLE KHOA
(
	MAKHOA VARCHAR(5),
	TENKHOA NVARCHAR(50),
	NAMTL INT,
	PHONG VARCHAR(5),
	DIENTHOAI VARCHAR (10),
	TRUONGKHOA VARCHAR(5),
	NGAYNHANCHUC DATE

	CONSTRAINT PK_K
	PRIMARY KEY(MAKHOA)
)

CREATE TABLE GIAOVIEN
(
	MAGV VARCHAR(5),
	HOTEN NVARCHAR(50),
	LUONG DECIMAL(10,2),
	PHAI NVARCHAR(5),
	NGSINH DATE,
	DIACHI NVARCHAR(50),
	GVQLCM VARCHAR (5),
	MABM VARCHAR(5)

	CONSTRAINT PK_GV
	PRIMARY KEY(MAGV)
)

CREATE TABLE NGUOITHAN
(
	MAGV VARCHAR(5),
	TEN NVARCHAR(50),
	NGSINH DATE,
	PHAI NVARCHAR(5),

	CONSTRAINT PK_NT
	PRIMARY KEY(MAGV,TEN)
)

CREATE TABLE BOMON
(
	MABM VARCHAR (5),
	TENBM NVARCHAR(50),
	PHONG VARCHAR(5),
	DIENTHOAI VARCHAR(10),
	TRUONGBM VARCHAR(5),
	MAKHOA VARCHAR(5),
	NGAYNHANCHUC DATE

	CONSTRAINT PK_BM
	PRIMARY KEY(MABM)
)

CREATE TABLE GV_DT
(
	MAGV VARCHAR(5),
	DIENTHOAI VARCHAR(10)

	CONSTRAINT PK_GV_DT
	PRIMARY KEY (MAGV,DIENTHOAI)
)
